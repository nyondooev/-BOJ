# 1칸 : 1개
# 2칸 존 : 6개 (2~7)
# 3칸 존 : 12개 (8~19)
# 4칸 존 : 18개 (20~37)
# 5칸 존 : 24개 (38 ~ 61)
# 6칸 존 : 30개 (62 ~ 91)
# ...
# 6의 배수가 누적되는 것
# 1 => 1칸
# 1 + 6 => 2칸
# 1 + 6 + 12 => 3칸
# 1 + 6 + 12 + 18 => 4칸
# 1 + 6 + 12 + 18 + 24 => 5칸

N = int(input())
i = 0
acc = 1
while True:
    if acc < N:
        i += 1
        acc += i * 6
    else:
        break
print(i + 1)